# 1. \n – Newline (line break)
print("Hello\nWorld")  
# Output:
# Hello
# World

# 2. \t – Tab (horizontal space)
print("Name:\tSiddharth")  
# Output: Name:	Siddharth

# 3. \\ – Backslash
print("C:\\Users\\Siddharth")  
# Output: C:\Users\Siddharth

# 4. \' – Single quote
print('It\'s Python')  
# Output: It's Python

# 5. \" – Double quote
print("She said \"Hello\"")  
# Output: She said "Hello"

# 6. \r – Carriage return (moves to line start, overwrites)
print("12345\rAB")  
# Output: AB345

# 7. \b – Backspace (removes one character)
print("Helloo\b!")  
# Output: Hello!



